package com.java.geraDTO.bean;

/**
 *
 * @author WILL
 */
public class ListTabela {
    
    private String tabela;
    private String tipoTabela;
    private String schemaTabela;
    private String catTable;

    /**
     * @return the tabela
     */
    public String getTabela() {
        return tabela;
    }

    /**
     * @param tabela the tabela to set
     */
    public void setTabela(String tabela) {
        this.tabela = tabela;
    }

    /**
     * @return the tipoTabela
     */
    public String getTipoTabela() {
        return tipoTabela;
    }

    /**
     * @param tipoTabela the tipoTabela to set
     */
    public void setTipoTabela(String tipoTabela) {
        this.tipoTabela = tipoTabela;
    }

    /**
     * @return the schemaTabela
     */
    public String getSchemaTabela() {
        return schemaTabela;
    }

    /**
     * @param schemaTabela the schemaTabela to set
     */
    public void setSchemaTabela(String schemaTabela) {
        this.schemaTabela = schemaTabela;
    }

    /**
     * @return the catTable
     */
    public String getCatTable() {
        return catTable;
    }

    /**
     * @param catTable the catTable to set
     */
    public void setCatTable(String catTable) {
        this.catTable = catTable;
    }
    
}
